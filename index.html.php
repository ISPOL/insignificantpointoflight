<h2>Python</h2>
<br />
<p>
In the summer of 2012 I attended the National High School Game Academy (NHSGA) at Carnegie Mellon University. 
There, I learned some of the basics of the Python programming language and the game design industry.
<br />
<br />
Python's core idea is simplicity and readability; other programming languages like Java can become a confusing muddle of brackets, while 
in Python, code is organized by indentation. The first picture is of Python while the second is Java.<br /> <br />
The Python is neat; it is organized by indentations and doesn't have many confusing pairs of brackets and parentheses.
Admittedly, it is somewhat cluttered as at the time, my group and I were rather stressed on time and hadn't had much experience with
naming conventions or even coding in general. <br /> <br />
The java has many brackets. It does come at a later point in my development; it looks more organized because of the comments and spacing. However, the Java
was much harder to keep organized and was more restrictive. 
<img src = "images/python code.jpg" width = "100%"><br />
<br /> <img src = "images/Java Code.jpg" width = "100%"> <br /> <br />

If I could summarize what I learned about programming and the games industry there, it would be thus:
<ol>
<li >An idea far more powerful than any programming code is the intent with which the code was to be used. Programming is just a tool used to 
achieve a result. It takes talent, drive, and luck far more than it takes programming skill or any other tool to succeed.</li> <br />
<li> Games are experiences. They are stories packaged with careful attention to detail and plot. Though the industry itself is rather inexperienced and young,
games have a future because the power of the game is in its interactivity-- imagine an interactive realm in which you, or your child, can explore at will.
It is the closest thing possible to living your favorite book or movie. Despite their current infantile stage, given continued advances in technology, 
gameshave the ability to revolutionize multimedia.
and entertainment.</li>
</ol>
</p>



